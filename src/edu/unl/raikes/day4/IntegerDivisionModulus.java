package edu.unl.raikes.day4;

/**
 * This is IntegerDivisionModulus.java, packaged within edu.unl.raikes.day4.
 */
public class IntegerDivisionModulus {

    /**
     * The main method.
     *
     * @param args This program does not accept any arguments.
     */
    public static void main(String[] args) {

        int integerResult = 27 / 5;
        System.out.println("result of integer division: " + integerResult);

        int modulusResult = 27 % 5;
        System.out.println("result of modulus: " + modulusResult);

        double doubleResult = 27.0 / 5;
        System.out.println("result of double division: " + doubleResult);
    }
}
